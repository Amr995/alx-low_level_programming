#include "3-calc.h"

/**
 * op_add - adds two integers
 * @a: the first int
 * @b: the second int
 *
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - adds two integers
 * @a: the first int
 * @b: the second int
 *
 * Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - adds two integers
 * @a: the first int
 * @b: the second int
 *
 * Return: the multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - adds two integers
 * @a: the first int
 * @b: the second int
 *
 * Return: the division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - adds tow integers
 * @a: the first int
 * @b: the second int
 *
 * Return: the modulo
 */
int op_mod(int a, int b)
{
	return (a % b);
}
